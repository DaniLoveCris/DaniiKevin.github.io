			<h2 style="color: skyblue" align="center">El productor y discjockey Vicente One More Time (Lugo, 1968) es, para los hijos del hardstyle, un padre que asumió la responsabilidad de abanderar la música de baile en nombre del In, local emblemático de la Costa Polvoranca desde 1993 hasta 2008. A comienzos de la década de los noventa, casi a mediados, Alcorcón fue una de las zonas más populares de fiesta en la zona sur de Madrid. Allí, como una institución del hardstyle, el In acogió a toda una cultura de la música electrónica que sobrevivió a través de varias generaciones hasta el 2008, cuando el último bakala recogía sus enseres. De aquellos fuegos de Costa Polvoranca no quedan ni las cenizas, pero Vicente One More Time permanece, a día de hoy, representando toda aquella memoria de la fiesta cada vez que se pone delante de miles de personas (sea en el Dreambeach o en el Medusa) o cuando alguien lo ve con Veronika Bows en el clip correspondiente al tema ‘Don’t waste your time’ (Blanco & Negro, 2015).</h2>
	
